#include <iostream>     
using namespace std;

int main(){
    int radius, Area; //declaration

    radius = 10; //initialization

    const int PI = 3.14;

    Area = PI * radius * radius;

    cout<<"Area of circle is: "<< Area;
}

//Variables- radius, Area
//Constant - PI
//Literals- 10, 3.14
//Keywords- int, const, main
//Identifiers- radius, Area
